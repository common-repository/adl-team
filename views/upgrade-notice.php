            <h3>Usage</h3>
                <p>Please follow the following steps to use and customize the plugin setting to meet your need.</p>
                <p>1. Add as many members or stuffs as you like from by clicking: “Team > Add New Member” option.</p>
                <p>2. Fill out all the fields of a member and save it.</p>
                <p>3. Now create a Team of members/stuffs by clicking: “Team > Shortcode Generator > Generate New Shortcode” option.</p>
                <p>4. Copy the generated dynamic shortcode (eg. [adl-team id=”123″] ) from the bottom of the page and use it on any page or post to show your team members/stuffs to your users/clients.</p>
                <p>Then the plugin will do the magic to display your team members beautifully in a responsive layout. This is how easy to use this plugin.</p>
